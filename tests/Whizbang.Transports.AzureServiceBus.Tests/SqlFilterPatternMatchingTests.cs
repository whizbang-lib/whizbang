using TUnit.Assertions;
using TUnit.Assertions.Extensions;
using TUnit.Core;

#pragma warning disable CA1707 // Identifiers should not contain underscores (test method names use underscores by convention)

namespace Whizbang.Transports.AzureServiceBus.Tests;

/// <summary>
/// Unit tests for SQL filter pattern matching.
/// These tests verify that the wildcard pattern translation works correctly
/// and that generated patterns will match expected Subject values.
/// </summary>
/// <remarks>
/// The Azure Service Bus SqlFilter uses SQL LIKE patterns:
/// - % matches zero or more characters
/// - _ matches exactly one character
///
/// RabbitMQ-style patterns are translated:
/// - # → %
/// - * → %
/// </remarks>
public class SqlFilterPatternMatchingTests {

  // ========================================
  // PATTERN TRANSLATION TESTS
  // ========================================
  // These tests verify that RabbitMQ-style patterns are correctly
  // translated to SQL LIKE patterns

  [Test]
  public async Task TranslatePattern_HashWildcard_TranslatesToPercentAsync() {
    // Arrange - RabbitMQ-style pattern with #
    var rabbitPattern = "jdx.contracts.chat.#";

    // Act
    var sqlPattern = _translateToSqlPattern(rabbitPattern);

    // Assert
    await Assert.That(sqlPattern).IsEqualTo("jdx.contracts.chat.%");
  }

  [Test]
  public async Task TranslatePattern_AsteriskWildcard_TranslatesToPercentAsync() {
    // Arrange - RabbitMQ-style pattern with *
    var rabbitPattern = "jdx.contracts.chat.*";

    // Act
    var sqlPattern = _translateToSqlPattern(rabbitPattern);

    // Assert
    await Assert.That(sqlPattern).IsEqualTo("jdx.contracts.chat.%");
  }

  [Test]
  public async Task TranslatePattern_StandaloneHash_TranslatesToPercentAsync() {
    // Arrange - Single # (match all)
    var rabbitPattern = "#";

    // Act
    var sqlPattern = _translateToSqlPattern(rabbitPattern);

    // Assert
    await Assert.That(sqlPattern).IsEqualTo("%");
  }

  [Test]
  public async Task TranslatePattern_MultiplePatterns_GeneratesOrExpressionAsync() {
    // Arrange - Multiple routing patterns
    var patterns = new[] {
      "whizbang.core.commands.system.#",
      "jdx.contracts.chat.#",
      "jdx.contracts.facts.#"
    };

    // Act - Build SQL expression
    var sqlExpression = _buildSqlFilterExpression(patterns);

    // Assert
    await Assert.That(sqlExpression).IsEqualTo(
      "[Subject] LIKE 'whizbang.core.commands.system.%' OR " +
      "[Subject] LIKE 'jdx.contracts.chat.%' OR " +
      "[Subject] LIKE 'jdx.contracts.facts.%'"
    );
  }

  // ========================================
  // PATTERN MATCHING SIMULATION TESTS
  // ========================================
  // These tests verify that the SQL LIKE pattern would match the Subject
  // values generated by TransportPublishStrategy

  [Test]
  public async Task SqlLikeMatch_CommandRoutingKey_MatchesPatternAsync() {
    // Arrange
    // TransportPublishStrategy generates: jdx.contracts.chat.activitytrackedcommand
    // SqlFilter pattern is: [Subject] LIKE 'jdx.contracts.chat.%'
    var subject = "jdx.contracts.chat.activitytrackedcommand";
    var pattern = "jdx.contracts.chat.%";

    // Act
    var matches = _sqlLikeMatches(subject, pattern);

    // Assert
    await Assert.That(matches).IsTrue()
      .Because("Command routing key should match SqlFilter pattern");
  }

  [Test]
  public async Task SqlLikeMatch_NestedClassCommand_MatchesPatternAsync() {
