  // ========================================
  // END-TO-END PATTERN VERIFICATION TESTS
  // ========================================
  // These tests verify the complete flow from namespace to pattern to match

  [Test]
  public async Task EndToEnd_ChatService_ReceivesChatCommandsAsync() {
    // Arrange - Chat service subscribes to these patterns
    var chatServicePatterns = new[] {
      "whizbang.core.commands.system.#",
      "jdx.contracts.chat.#"
    };

    // Commands that should be received by Chat service
    var chatCommands = new[] {
      "jdx.contracts.chat.activitytrackedcommand",
      "jdx.contracts.chat.chatconversationscontracts+createcommand",
      "whizbang.core.commands.system.healthcheckcommand"
    };

    // Commands that should NOT be received by Chat service
    var otherCommands = new[] {
      "jdx.contracts.auth.authcontracts+createtenantcommand",
      "jdx.contracts.bff.createpageviewcommand",
      "message" // Default when RoutingKey is null
    };

    // Act & Assert - All chat commands should match
    var sqlPatterns = chatServicePatterns.Select(_translateToSqlPattern).ToList();

    foreach (var cmd in chatCommands) {
      var matches = sqlPatterns.Any(p => _sqlLikeMatches(cmd, p));
      await Assert.That(matches).IsTrue()
        .Because($"Chat service SHOULD receive command: {cmd}");
    }

    // Act & Assert - Other commands should NOT match
    foreach (var cmd in otherCommands) {
      var matches = sqlPatterns.Any(p => _sqlLikeMatches(cmd, p));
      await Assert.That(matches).IsFalse()
        .Because($"Chat service should NOT receive command: {cmd}");
    }
  }

  [Test]
  public async Task EndToEnd_BFFService_ReceivesBFFCommandsAsync() {
    // Arrange - BFF service subscribes to these patterns
    var bffServicePatterns = new[] {
