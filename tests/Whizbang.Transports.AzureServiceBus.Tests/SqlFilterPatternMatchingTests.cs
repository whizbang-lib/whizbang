  }

  // ========================================
  // HELPER METHODS
  // ========================================

  /// <summary>
  /// Translates RabbitMQ-style wildcard patterns to SQL LIKE patterns.
  /// This mirrors the logic in AzureServiceBusTransport._applyRoutingPatternFilterAsync
  /// </summary>
  private static string _translateToSqlPattern(string rabbitPattern) {
    return rabbitPattern
      .Replace(".#", ".%")
      .Replace(".*", ".%")
      .Replace("#", "%")
      .Replace("*", "%");
  }

  /// <summary>
  /// Builds the SQL filter expression for multiple patterns.
  /// This mirrors the logic in AzureServiceBusTransport._applyRoutingPatternFilterAsync
  /// </summary>
  private static string _buildSqlFilterExpression(IEnumerable<string> patterns) {
    var likePatterns = patterns
      .Select(_translateToSqlPattern)
      .Select(p => $"[Subject] LIKE '{p}'");
    return string.Join(" OR ", likePatterns);
  }

  /// <summary>
  /// Simulates SQL LIKE pattern matching.
  /// Note: This is a simplified implementation that handles % wildcard only.
  /// </summary>
  private static bool _sqlLikeMatches(string subject, string pattern) {
    // Handle simple % patterns (matches zero or more characters)
    if (pattern.EndsWith('%')) {
      var prefix = pattern[..^1];
      return subject.StartsWith(prefix, StringComparison.Ordinal);
    }

    // Exact match if no wildcard
    return string.Equals(subject, pattern, StringComparison.Ordinal);
  }
}
