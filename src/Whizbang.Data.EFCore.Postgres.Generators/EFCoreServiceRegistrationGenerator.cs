  public void Initialize(IncrementalGeneratorInitializationContext context) {
    // Generate marker file to confirm generator is running
    context.RegisterPostInitializationOutput(ctx => {
      ctx.AddSource("_EFCoreGenerator_Initialized.g.cs",
        $"// EFCoreServiceRegistrationGenerator initialized at {System.DateTime.UtcNow:yyyy-MM-dd HH:mm:ss} UTC\n" +
        "// Looking for: IPerspectiveFor<TModel> interfaces");
    });

    // Extract table name configuration from MSBuild properties (WhizbangStripTableNameSuffixes, etc.)
    var tableNameConfig = context.AnalyzerConfigOptionsProvider.Select(
        ConfigurationUtilities.SelectTableNameConfig
    );

    // Discover all perspective classes that implement IPerspectiveFor<TModel>
    // Phase 1: Extract candidates (config-independent data only)
    var perspectiveCandidates = context.SyntaxProvider.CreateSyntaxProvider(
        predicate: static (node, _) => node is ClassDeclarationSyntax { BaseList.Types.Count: > 0 },
        transform: static (ctx, ct) => _extractPerspectiveModelCandidate(ctx, ct)
    ).Where(static info => info is not null);

    // Phase 2: Combine candidates with config and build final PerspectiveModelInfo
    var perspectives = perspectiveCandidates.Combine(tableNameConfig)
        .Select(static (pair, _) => _buildPerspectiveModelInfo(pair.Left!, pair.Right))
        .Where(static info => info is not null);

    // Discover DbContext classes
    var dbContextClasses = context.SyntaxProvider.CreateSyntaxProvider(
        predicate: static (node, _) => node is ClassDeclarationSyntax { BaseList.Types.Count: > 0 },
        transform: static (ctx, ct) => _extractDbContextInfo(ctx, ct)
    ).Where(static info => info is not null);

    // Discover multi-model ILensQuery<T1, T2, ...> constructor parameters
    var multiLensQueries = context.SyntaxProvider.CreateSyntaxProvider(
        predicate: static (node, _) =>
            node is ParameterSyntax { Type: GenericNameSyntax { TypeArgumentList.Arguments.Count: >= 2 } },
        transform: static (ctx, ct) => _extractMultiLensQueryInfo(ctx, ct)
    ).Where(static info => info is not null);

    // Combine perspectives with DbContext info and compilation
    var allData = perspectives.Collect()
        .Combine(dbContextClasses.Collect())
        .Combine(context.CompilationProvider);

    // Combine perspectives, DbContexts, and multi-lens queries for registration metadata generation
    var registrationData = perspectives.Collect()
        .Combine(dbContextClasses.Collect())
        .Combine(multiLensQueries.Collect());

    // Generate DbContext partial class with DbSet<PerspectiveRow<TModel>> properties
    context.RegisterSourceOutput(
        allData,
        static (ctx, data) => {
          var perspectives = data.Left.Left;
          var dbContexts = data.Left.Right;

          try {
            // Filter nulls to ensure type safety - OfType<> both filters and changes type to non-nullable
            var validPerspectives = perspectives.OfType<PerspectiveModelInfo>().ToImmutableArray();
            var validDbContexts = dbContexts.OfType<DbContextInfo>().ToImmutableArray();

            // Always report count, even if zero (for debugging)
            var descriptor = new DiagnosticDescriptor(
                id: "EFCORE104",
                title: "EFCore Generator Running",
                messageFormat: "EFCoreServiceRegistrationGenerator found {0} perspective(s) with models",
                category: DIAGNOSTIC_CATEGORY,
                defaultSeverity: DiagnosticSeverity.Info,
                isEnabledByDefault: true);
            ctx.ReportDiagnostic(Diagnostic.Create(descriptor, Location.None, validPerspectives.Length));

            // Report each discovered perspective
            foreach (var perspective in validPerspectives) {
              var modelDescriptor = new DiagnosticDescriptor(
                  id: "EFCORE105",
                  title: "Perspective Model Discovered",
                  messageFormat: "Found perspective {0} with model {1} (table: {2})",
                  category: DIAGNOSTIC_CATEGORY,
                  defaultSeverity: DiagnosticSeverity.Info,
                  isEnabledByDefault: true);
              ctx.ReportDiagnostic(Diagnostic.Create(modelDescriptor, Location.None,
                  perspective.PerspectiveClassName,
                  perspective.ModelTypeName,
                  perspective.TableName));
            }

            _generateDbContextPartial(ctx, validPerspectives, validDbContexts);
          } catch (Exception ex) {
            var descriptor = new DiagnosticDescriptor(
                id: "EFCORE997",
                title: EFCORE_GENERATOR_ERROR_TITLE,
                messageFormat: "Error in GenerateDbContextPartial: {0}",
                category: DIAGNOSTIC_CATEGORY,
                defaultSeverity: DiagnosticSeverity.Error,
                isEnabledByDefault: true);
            ctx.ReportDiagnostic(Diagnostic.Create(descriptor, Location.None, ex.Message));
          }
        }
    );

    // Generate EFCoreRegistrationMetadata class (uses registrationData which includes multi-lens queries)
    context.RegisterSourceOutput(
        registrationData,
        static (ctx, data) => {
          var perspectives = data.Left.Left;
          var dbContexts = data.Left.Right;
          var multiLensQueries = data.Right;

          try {
            // Filter nulls to ensure type safety - OfType<> both filters and changes type to non-nullable
            var validPerspectives = perspectives.OfType<PerspectiveModelInfo>().ToImmutableArray();
            var validDbContexts = dbContexts.OfType<DbContextInfo>().ToImmutableArray();
            var validMultiLensQueries = multiLensQueries.OfType<MultiLensQueryInfo>().ToImmutableArray();

            _generateRegistrationMetadata(ctx, validPerspectives, validDbContexts, validMultiLensQueries);
          } catch (Exception ex) {
            var descriptor = new DiagnosticDescriptor(
                id: "EFCORE996",
                title: EFCORE_GENERATOR_ERROR_TITLE,
                messageFormat: "Error in GenerateRegistrationMetadata: {0}",
                category: DIAGNOSTIC_CATEGORY,
                defaultSeverity: DiagnosticSeverity.Error,
                isEnabledByDefault: true);
            ctx.ReportDiagnostic(Diagnostic.Create(descriptor, Location.None, ex.Message));
          }
        }
    );

    // Generate DbContext schema extensions (EnsureWhizbangTablesCreatedAsync)
    context.RegisterSourceOutput(
        allData,
        static (ctx, data) => {
          var perspectives = data.Left.Left;
          var dbContexts = data.Left.Right;

          try {
            // Filter nulls to ensure type safety - OfType<> both filters and changes type to non-nullable
            var validPerspectives = perspectives.OfType<PerspectiveModelInfo>().ToImmutableArray();
            var validDbContexts = dbContexts.OfType<DbContextInfo>().ToImmutableArray();
            var compilation = data.Right;

            _generateSchemaExtensions(ctx, validPerspectives, validDbContexts, compilation);
          } catch (Exception ex) {
            var descriptor = new DiagnosticDescriptor(
                id: "EFCORE995",
                title: EFCORE_GENERATOR_ERROR_TITLE,
                messageFormat: "Error in GenerateSchemaExtensions: {0}",
                category: DIAGNOSTIC_CATEGORY,
                defaultSeverity: DiagnosticSeverity.Error,
                isEnabledByDefault: true);
            ctx.ReportDiagnostic(Diagnostic.Create(descriptor, Location.None, ex.Message));
          }
        }
    );

    // Generate turnkey DbContext extension methods (Add{DbContextName})
    context.RegisterSourceOutput(
        allData,
        static (ctx, data) => {
          var perspectives = data.Left.Left;
          var dbContexts = data.Left.Right;

          try {
            var validPerspectives = perspectives.OfType<PerspectiveModelInfo>().ToImmutableArray();
            var validDbContexts = dbContexts.OfType<DbContextInfo>().ToImmutableArray();

            _generateTurnkeyExtensions(ctx, validPerspectives, validDbContexts);
          } catch (Exception ex) {
            var descriptor = new DiagnosticDescriptor(
                id: "EFCORE994",
                title: EFCORE_GENERATOR_ERROR_TITLE,
                messageFormat: "Error in GenerateTurnkeyExtensions: {0}",
                category: DIAGNOSTIC_CATEGORY,
                defaultSeverity: DiagnosticSeverity.Error,
                isEnabledByDefault: true);
            ctx.ReportDiagnostic(Diagnostic.Create(descriptor, Location.None, ex.Message));
          }
        }
    );
  }

  /// <summary>
  /// Extracts DbContext information from a class inheriting from DbContext.
  /// Returns null if the class doesn't inherit from DbContext OR doesn't have [WhizbangDbContext] attribute (opt-in required).
  /// </summary>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_WithWhizbangDbContextAttribute_DiscoversDbContextAsync</tests>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_WithoutWhizbangDbContextAttribute_DoesNotDiscoverDbContextAsync</tests>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_WithDefaultKey_UsesEmptyStringKeyAsync</tests>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_WithSingleKey_DiscoversDbContextWithKeyAsync</tests>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_WithMultipleKeys_DiscoversDbContextWithAllKeysAsync</tests>
  private static DbContextInfo? _extractDbContextInfo(
      GeneratorSyntaxContext context,
      CancellationToken ct) {
    var classDecl = (ClassDeclarationSyntax)context.Node;

    if (context.SemanticModel.GetDeclaredSymbol(classDecl, ct) is not INamedTypeSymbol symbol) {
      return null;
    }

    // Check if class inherits from DbContext
    var baseType = symbol.BaseType;
    bool inheritsDbContext = false;
    while (baseType != null) {
      if (baseType.ToDisplayString() == "Microsoft.EntityFrameworkCore.DbContext") {
        inheritsDbContext = true;
        break;
      }
      baseType = baseType.BaseType;
    }

    if (!inheritsDbContext) {
      return null;
    }

    // Check for [WhizbangDbContext] attribute (explicit opt-in required)
    var attribute = symbol.GetAttributes()
        .FirstOrDefault(a => a.AttributeClass?.ToDisplayString() == "Whizbang.Data.EFCore.Custom.WhizbangDbContextAttribute");

    if (attribute is null) {
      return null;  // No attribute = not discovered (opt-in required)
    }

    // Extract keys from attribute
    var keys = _extractKeysFromAttribute(attribute);
    if (keys.Length == 0) {
      keys = [""];  // Default to unnamed key
    }

    // Extract schema from attribute's Schema property, or derive from namespace if not specified
    var schema = _extractSchemaFromAttribute(attribute);
    if (string.IsNullOrEmpty(schema)) {
      schema = _deriveSchemaFromNamespace(symbol.ContainingNamespace.ToDisplayString());
    }

    // Extract connection string name from attribute, or derive from class name
    var connectionStringName = _extractConnectionStringNameFromAttribute(attribute)
        ?? _deriveConnectionStringName(symbol.Name);

    return new DbContextInfo(
        ClassName: symbol.Name,
        FullyQualifiedName: symbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat),
        Namespace: symbol.ContainingNamespace.ToDisplayString(),
        Schema: schema ?? "public", // Should never be null, but satisfy compiler
        Keys: keys,
        ConnectionStringName: connectionStringName
    );
  }

  /// <summary>
  /// Extracts the Schema property from [WhizbangDbContext] attribute.
  /// Returns null if Schema property is not set.
  /// </summary>
  private static string? _extractSchemaFromAttribute(AttributeData attribute) {
    // Look for Schema named property
    var schemaProp = attribute.NamedArguments
        .FirstOrDefault(kvp => kvp.Key == "Schema");

    if (schemaProp.Key == "Schema" && schemaProp.Value.Value is string schemaValue) {
      return schemaValue;
    }

    return null;
  }

  private static string? _extractConnectionStringNameFromAttribute(AttributeData attribute) {
    // Look for ConnectionStringName named property
    var connProp = attribute.NamedArguments
        .FirstOrDefault(kvp => kvp.Key == "ConnectionStringName");

    if (connProp.Key == "ConnectionStringName" && connProp.Value.Value is string connValue) {
      return connValue;
    }

    return null;
  }

  private static string _deriveConnectionStringName(string className) {
    // Derive connection string name from DbContext name by convention:
    // "ChatDbContext" -> "chat-db" (remove DbContext suffix, lowercase, add -db)
    var name = className.EndsWith("DbContext", StringComparison.Ordinal)
        ? className[..^9]
        : className;
    return name.ToLowerInvariant() + "-db";
  }

  /// <summary>
  /// Derives PostgreSQL schema name from DbContext namespace.
  /// Examples:
  /// - "ECommerce.InventoryWorker" → "inventory"
  /// - "ECommerce.BFF.API" → "bff"
  /// - "MyApp.OrderService" → "order"
  /// </summary>
  private static string _deriveSchemaFromNamespace(string namespaceName) {
    if (string.IsNullOrEmpty(namespaceName)) {
      return "public"; // Default PostgreSQL schema
    }

    // Split namespace into segments
    var segments = namespaceName.Split('.');

    // Take the last segment (e.g., "InventoryWorker", "API")
    var lastSegment = segments[segments.Length - 1];

    // If last segment is generic (API, Service, etc.), take second-to-last
    if ((lastSegment.Equals("API", StringComparison.OrdinalIgnoreCase) ||
         lastSegment.Equals("Service", StringComparison.OrdinalIgnoreCase) ||
         lastSegment.Equals("Worker", StringComparison.OrdinalIgnoreCase)) &&
        segments.Length > 1) {
      lastSegment = segments[segments.Length - 2];
    }

    // Remove common suffixes (case-insensitive)
    // Use regex for case-insensitive replacement (netstandard2.0 doesn't have String.Replace with StringComparison)
    // Timeout added to prevent ReDoS attacks (S6444)
    lastSegment = System.Text.RegularExpressions.Regex.Replace(lastSegment, "Worker", "", System.Text.RegularExpressions.RegexOptions.IgnoreCase, TimeSpan.FromSeconds(1));
    lastSegment = System.Text.RegularExpressions.Regex.Replace(lastSegment, "Service", "", System.Text.RegularExpressions.RegexOptions.IgnoreCase, TimeSpan.FromSeconds(1));

    // Convert to lowercase
    return lastSegment.ToLowerInvariant();
  }

  /// <summary>
  /// Quotes a PostgreSQL identifier to handle reserved keywords (e.g., "user", "table", "select").
  /// Always quotes to ensure safety regardless of the identifier value.
  /// Example: "user" → "\"user\"", "bff" → "\"bff\""
  /// </summary>
  private static string _quotePostgresIdentifier(string identifier) {
    // Double quotes are the PostgreSQL standard for quoting identifiers
    // This handles reserved keywords like "user", "table", "select", etc.
    return $"\"{identifier}\"";
  }

  /// <summary>
  /// Extracts perspective candidate information from a class implementing IPerspectiveFor.
  /// Discovers TModel type from IPerspectiveFor&lt;TModel&gt; base interface (first type argument).
  /// Returns null if the class doesn't implement the interface.
  /// This is Phase 1 of the pipeline - extracts config-independent data only.
  /// </summary>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_WithDiscoveredDbContext_GeneratesPartialClassAsync</tests>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_WithDiscoveredDbContext_GeneratesRegistrationMetadataAsync</tests>
  private static PerspectiveModelCandidate? _extractPerspectiveModelCandidate(
      GeneratorSyntaxContext context,
      CancellationToken ct) {
    var classDecl = (ClassDeclarationSyntax)context.Node;

    if (context.SemanticModel.GetDeclaredSymbol(classDecl, ct) is not INamedTypeSymbol symbol) {
      return null;
    }

    // Check if class implements IPerspectiveFor<TModel> base interface
    var perspectiveForInterface = symbol.AllInterfaces.FirstOrDefault(i => {
      var originalDef = i.OriginalDefinition.ToDisplayString();
      return originalDef == "Whizbang.Core.Perspectives.IPerspectiveFor<TModel>" ||
             originalDef == "Whizbang.Core.Perspectives.IPerspectiveWithActionsFor<TModel>" ||
             originalDef == "Whizbang.Core.Perspectives.IPerspectiveBase<TModel>";
    });

    if (perspectiveForInterface is null) {
      return null; // Not a perspective
    }

    // Perspective discovered - extract TModel from first type argument
    var modelType = perspectiveForInterface.TypeArguments[0];
    var tableBaseName = TypeNameUtilities.GetTableBaseName(modelType);
    var dbSetPropertyName = TypeNameUtilities.GetDbSetPropertyName(modelType);

    // Extract physical fields from model type
    var physicalFields = _extractPhysicalFields(modelType as INamedTypeSymbol);

    // Check for [WhizbangPerspective] attribute (optional)
    var perspectiveAttribute = symbol.GetAttributes()
        .FirstOrDefault(a => a.AttributeClass?.ToDisplayString() == "Whizbang.Core.Perspectives.WhizbangPerspectiveAttribute");

    string[] keys;
    if (perspectiveAttribute is not null) {
      // Attribute present - extract keys
      keys = _extractKeysFromAttribute(perspectiveAttribute);
    } else {
      // No attribute - matches default DbContext only
      keys = [];
    }

    return new PerspectiveModelCandidate(
        PerspectiveClassName: symbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat),
        ModelTypeName: modelType.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat),
        DbSetPropertyName: dbSetPropertyName,
        TableBaseName: tableBaseName,
        NamespaceHint: symbol.ContainingNamespace.ToDisplayString(),
        Keys: keys,
        PhysicalFields: physicalFields
    );
  }

  /// <summary>
  /// Builds final PerspectiveModelInfo from candidate by applying table name configuration.
  /// This is Phase 2 of the pipeline - applies config-dependent table name generation.
  /// </summary>
  /// <param name="candidate">The perspective candidate with config-independent data</param>
  /// <param name="config">Table name configuration from MSBuild properties</param>
  /// <returns>Final PerspectiveModelInfo with generated table name</returns>
  private static PerspectiveModelInfo? _buildPerspectiveModelInfo(
      PerspectiveModelCandidate candidate,
      TableNameConfig config) {

    // Apply table name configuration to generate final table name
    var tableName = NamingConventionUtilities.GenerateTableName(candidate.TableBaseName, config);

    return new PerspectiveModelInfo(
        PerspectiveClassName: candidate.PerspectiveClassName,
        ModelTypeName: candidate.ModelTypeName,
        DbSetPropertyName: candidate.DbSetPropertyName,
        TableName: tableName,
        NamespaceHint: candidate.NamespaceHint,
        Keys: candidate.Keys,
        PhysicalFields: candidate.PhysicalFields
    );
  }

  /// <summary>
  /// Extracts string array keys from attribute constructor arguments.
  /// Supports params string[] parameter pattern used by WhizbangDbContext and WhizbangPerspective attributes.
  /// </summary>
  /// <param name="attribute">The attribute data to extract keys from</param>
  /// <returns>Array of keys, or empty array if no keys found</returns>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_WithDefaultKey_UsesEmptyStringKeyAsync</tests>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_WithSingleKey_DiscoversDbContextWithKeyAsync</tests>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_WithMultipleKeys_DiscoversDbContextWithAllKeysAsync</tests>
  private static string[] _extractKeysFromAttribute(AttributeData attribute) {
    if (attribute.ConstructorArguments.Length == 0) {
      return [];
    }

    var arg = attribute.ConstructorArguments[0];

    // Handle params array argument
    if (arg.Kind == TypedConstantKind.Array) {
      return [.. arg.Values
          .Where(v => v.Value is string)
          .Select(v => (string)v.Value!)];
    }

    return [];
  }

  /// <summary>
  /// Extracts physical field information from a model type.
  /// Looks for [PhysicalField] and [VectorField] attributes on properties.
  /// </summary>
  private static ImmutableArray<PhysicalFieldInfo> _extractPhysicalFields(INamedTypeSymbol? modelType) {
    if (modelType is null) {
      return [];
    }

    var physicalFields = new System.Collections.Generic.List<PhysicalFieldInfo>();
    var properties = modelType.GetMembers()
        .OfType<IPropertySymbol>()
        .Where(p => !p.IsStatic);

    foreach (var property in properties) {
      var physicalFieldAttr = property.GetAttributes()
          .FirstOrDefault(a => a.AttributeClass?.ToDisplayString() == "Whizbang.Core.Perspectives.PhysicalFieldAttribute");
      var vectorFieldAttr = property.GetAttributes()
          .FirstOrDefault(a => a.AttributeClass?.ToDisplayString() == "Whizbang.Core.Perspectives.VectorFieldAttribute");

      if (physicalFieldAttr is not null) {
        var info = _extractPhysicalFieldInfo(property, physicalFieldAttr);
        if (info is not null) {
          physicalFields.Add(info);
        }
      } else if (vectorFieldAttr is not null) {
        var info = _extractVectorFieldInfo(property, vectorFieldAttr);
        if (info is not null) {
          physicalFields.Add(info);
        }
      }
    }

    return [.. physicalFields];
  }

  /// <summary>
  /// Extracts PhysicalFieldInfo from a [PhysicalField] attribute.
  /// </summary>
  private static PhysicalFieldInfo? _extractPhysicalFieldInfo(IPropertySymbol property, AttributeData attribute) {
    var propertyName = property.Name;
    var typeName = property.Type.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);

    // Extract named arguments
    bool isIndexed = false;
    bool isUnique = false;
    string? columnName = null;

    foreach (var namedArg in attribute.NamedArguments) {
      switch (namedArg.Key) {
        case "Indexed":
          isIndexed = namedArg.Value.Value is true;
          break;
        case "Unique":
          isUnique = namedArg.Value.Value is true;
          break;
        case "ColumnName":
          columnName = namedArg.Value.Value as string;
          break;
      }
    }

    // Generate column name from property name if not specified
    var finalColumnName = columnName ?? NamingConventionUtilities.ToSnakeCase(propertyName);

    return new PhysicalFieldInfo(
        PropertyName: propertyName,
        ColumnName: finalColumnName,
        TypeName: typeName,
        IsIndexed: isIndexed,
        IsUnique: isUnique,
        MaxLength: null, // Not applicable for physical fields (JSONB handles length)
        IsVector: false,
        VectorDimensions: null,
        VectorDistanceMetric: null,
        VectorIndexType: null,
        VectorIndexLists: null
    );
  }

  /// <summary>
  /// Extracts PhysicalFieldInfo from a [VectorField] attribute.
  /// </summary>
  private static PhysicalFieldInfo? _extractVectorFieldInfo(IPropertySymbol property, AttributeData attribute) {
    var propertyName = property.Name;
    var typeName = property.Type.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);

    // Extract constructor argument (dimensions)
    int? dimensions = null;
    if (attribute.ConstructorArguments.Length > 0 && attribute.ConstructorArguments[0].Value is int dim) {
      dimensions = dim;
    }

    // Extract named arguments
    bool isIndexed = true; // Vectors are typically indexed
    string? columnName = null;
    GeneratorVectorDistanceMetric? distanceMetric = GeneratorVectorDistanceMetric.Cosine; // Default
    GeneratorVectorIndexType? indexType = GeneratorVectorIndexType.IVFFlat; // Default
    int? indexLists = null;

    foreach (var namedArg in attribute.NamedArguments) {
      switch (namedArg.Key) {
        case "Indexed":
          isIndexed = namedArg.Value.Value is true;
          break;
        case "ColumnName":
          columnName = namedArg.Value.Value as string;
          break;
        case "DistanceMetric":
          if (namedArg.Value.Value is int metricInt) {
            distanceMetric = (GeneratorVectorDistanceMetric)metricInt;
          }
          break;
        case "IndexType":
          if (namedArg.Value.Value is int indexTypeInt) {
            indexType = (GeneratorVectorIndexType)indexTypeInt;
          }
          break;
        case "IndexLists":
          if (namedArg.Value.Value is int lists) {
            indexLists = lists;
          }
          break;
      }
    }

    // Generate column name from property name if not specified
    var finalColumnName = columnName ?? NamingConventionUtilities.ToSnakeCase(propertyName);

    return new PhysicalFieldInfo(
        PropertyName: propertyName,
        ColumnName: finalColumnName,
        TypeName: typeName,
        IsIndexed: isIndexed,
        IsUnique: false, // Vectors are never unique
        MaxLength: null, // Not applicable for vectors
        IsVector: true,
        VectorDimensions: dimensions,
        VectorDistanceMetric: distanceMetric,
        VectorIndexType: indexType,
        VectorIndexLists: indexLists
    );
  }

  /// <summary>
  /// Extracts multi-model ILensQuery information from a constructor parameter.
  /// Returns null if the parameter is not typed as ILensQuery&lt;T1, T2, ...&gt; (arity >= 2)
  /// from Whizbang.Core.Lenses.
  /// </summary>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_WithMultiModelLensQueryConstructorParam_GeneratesRegistrationAsync</tests>
  private static MultiLensQueryInfo? _extractMultiLensQueryInfo(
      GeneratorSyntaxContext context,
      CancellationToken ct) {
    var parameterSyntax = (ParameterSyntax)context.Node;
    if (parameterSyntax.Type is not GenericNameSyntax genericName) {
      return null;
    }

    // Get the semantic type info for the parameter type
    var typeInfo = context.SemanticModel.GetTypeInfo(genericName, ct);
    if (typeInfo.Type is not INamedTypeSymbol type) {
      return null;
    }

    // Check if it's ILensQuery from Whizbang.Core.Lenses with 2+ type arguments
    if (type.TypeKind != TypeKind.Interface) {
      return null;
    }

    if (!type.Name.Equals("ILensQuery", StringComparison.Ordinal)) {
      return null;
    }

    if (type.TypeArguments.Length < 2) {
      return null;
    }

    if (type.ContainingNamespace?.ToDisplayString() != "Whizbang.Core.Lenses") {
      return null;
    }

    // Extract model type names (fully qualified with global:: prefix)
    var modelTypeNames = type.TypeArguments
        .Select(t => t.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat))
        .ToImmutableArray();

    // Get consumer class name from containing type declaration
    var containingClass = parameterSyntax.FirstAncestorOrSelf<TypeDeclarationSyntax>();
    string consumerClassName = "Unknown";
    if (containingClass != null) {
      var classSymbol = context.SemanticModel.GetDeclaredSymbol(containingClass, ct);
      if (classSymbol != null) {
        consumerClassName = classSymbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
      }
    }

    return new MultiLensQueryInfo(modelTypeNames, type.TypeArguments.Length, consumerClassName);
  }

  /// <summary>
  /// Generates multi-model ILensQuery transient registrations for a DbContext.
  /// Generates PhysicalFieldHydratorRegistry.Register call for a model with physical fields.
  /// The hydrator reads shadow property values via MaterializationInterceptionData.GetPropertyValue
  /// and copies them into the Data model (AOT-safe, no reflection).
  /// </summary>
  private static void _generatePhysicalFieldHydratorRegistration(
      StringBuilder sb,
      PerspectiveModelInfo model) {

    sb.AppendLine($"        // Register physical field hydrator for {model.ModelTypeName}");
    sb.AppendLine($"        Whizbang.Data.EFCore.Postgres.PhysicalFieldHydratorRegistry.Register<{model.ModelTypeName}>((materializationData, entity) => {{");
    sb.AppendLine($"          var row = (global::Whizbang.Core.Lenses.PerspectiveRow<{model.ModelTypeName}>)entity;");

    foreach (var field in model.PhysicalFields) {
      if (field.IsVector) {
        // Vector fields: GetPropertyValue returns Pgvector.Vector, convert to float[]
        sb.AppendLine($"          var _{field.ColumnName} = materializationData.GetPropertyValue<global::Pgvector.Vector?>(\"{field.ColumnName}\");");
        sb.AppendLine($"          if (_{field.ColumnName} is not null) {{");
        sb.AppendLine($"            row.Data.{field.PropertyName} = _{field.ColumnName}.ToArray();");
        sb.AppendLine($"          }}");
      } else {
        // Non-vector fields: direct type cast
        var clrType = field.TypeName;
        var isNullable = clrType.EndsWith("?", StringComparison.Ordinal);
        if (isNullable) {
          sb.AppendLine($"          var _{field.ColumnName} = materializationData.GetPropertyValue<{clrType}>(\"{field.ColumnName}\");");
          sb.AppendLine($"          if (_{field.ColumnName} is not null) {{");
          sb.AppendLine($"            row.Data.{field.PropertyName} = _{field.ColumnName};");
          sb.AppendLine($"          }}");
        } else {
          sb.AppendLine($"          row.Data.{field.PropertyName} = materializationData.GetPropertyValue<{clrType}>(\"{field.ColumnName}\");");
        }
      }
    }

    sb.AppendLine("        });");
    sb.AppendLine();
  }

  /// <summary>
  /// Generates multi-model ILensQuery registrations for perspective lens queries.
  /// Cross-references model types against known perspectives to get table names.
  /// Reports WHIZ401 for unknown models, WHIZ402 for successful auto-detection.
  /// </summary>
  private static void _generateMultiLensQueryRegistrations(
      SourceProductionContext context,
      StringBuilder sb,
      DbContextInfo dbContext,
      List<PerspectiveModelInfo> matchingPerspectives,
      ImmutableArray<MultiLensQueryInfo> multiLensQueries) {

    if (multiLensQueries.IsEmpty) {
      return;
    }

    // Build lookup from model type name to table name for this DbContext's perspectives
    // S3267: Loop has side effects (dictionary mutation with last-wins semantics) — LINQ not appropriate
#pragma warning disable S3267
    var modelTableLookup = new Dictionary<string, string>();
    foreach (var perspective in matchingPerspectives) {
      modelTableLookup[perspective.ModelTypeName] = perspective.TableName;
    }
#pragma warning restore S3267

    // Deduplicate multi-lens queries by their model type combination (preserve declaration order)
    var uniqueCombinations = multiLensQueries
        .GroupBy(q => string.Join(",", q.ModelTypeNames))
        .Select(g => g.First())
        .ToList();

    foreach (var query in uniqueCombinations) {
      // Check if ALL model types are known perspectives for this DbContext
      var allModelsKnown = true;
      foreach (var modelType in query.ModelTypeNames) {
        if (!modelTableLookup.ContainsKey(modelType)) {
          allModelsKnown = false;

          // Report WHIZ401 - unknown model type
          var typeArgDisplay = string.Join(", ", query.ModelTypeNames.Select(t => t.Replace(PLACEHOLDER_GLOBAL, "")));
          var unknownType = modelType.Replace(PLACEHOLDER_GLOBAL, "");
          context.ReportDiagnostic(Diagnostic.Create(
              DiagnosticDescriptors.MultiLensQueryUnknownModel,
              Location.None,
              typeArgDisplay,
              query.ConsumerClassName.Replace(PLACEHOLDER_GLOBAL, ""),
              unknownType));
          break;
        }
      }

      if (!allModelsKnown) {
        continue;
      }

      // Report WHIZ402 - auto-detected
      var modelNames = string.Join(", ", query.ModelTypeNames.Select(t => t.Replace(PLACEHOLDER_GLOBAL, "")));
      context.ReportDiagnostic(Diagnostic.Create(
          DiagnosticDescriptors.MultiLensQueryDiscovered,
          Location.None,
          modelNames,
          query.Arity,
          query.ConsumerClassName.Replace(PLACEHOLDER_GLOBAL, "")));

      // Generate the transient registration code
      var typeArgs = string.Join(", ", query.ModelTypeNames);
      sb.AppendLine($"        // Auto-detected: ILensQuery<{modelNames}> (from {query.ConsumerClassName.Replace(PLACEHOLDER_GLOBAL, "")})");
      sb.AppendLine($"        services.AddTransient<Whizbang.Core.Lenses.ILensQuery<{typeArgs}>>(sp => {{");
      sb.AppendLine($"          var factory = sp.GetRequiredService<Microsoft.EntityFrameworkCore.IDbContextFactory<{dbContext.FullyQualifiedName}>>();");
      sb.AppendLine("          var context = factory.CreateDbContext();");
      sb.AppendLine("          var tableNames = new System.Collections.Generic.Dictionary<System.Type, string> {");

      for (int i = 0; i < query.ModelTypeNames.Length; i++) {
        var modelType = query.ModelTypeNames[i];
        var tableName = modelTableLookup[modelType];
        var comma = i < query.ModelTypeNames.Length - 1 ? "," : "";
        sb.AppendLine($"            [typeof({modelType})] = \"{tableName}\"{comma}");
      }

      sb.AppendLine("          };");
      sb.AppendLine("          var scopeContextAccessor = sp.GetRequiredService<global::Whizbang.Core.Security.IScopeContextAccessor>();");
      sb.AppendLine("          var whizbangOptions = sp.GetRequiredService<Microsoft.Extensions.Options.IOptions<global::Whizbang.Core.Configuration.WhizbangCoreOptions>>();");
      sb.AppendLine($"          return new Whizbang.Data.EFCore.Postgres.EFCorePostgresLensQuery<{typeArgs}>(context, tableNames, scopeContextAccessor, whizbangOptions);");
      sb.AppendLine("        });");
      sb.AppendLine();
    }
  }

  /// <summary>
  /// Converts a PhysicalFieldInfo to the corresponding PostgreSQL column type.
  /// </summary>
  private static string _getPostgresColumnType(PhysicalFieldInfo field) {
    // Handle vector fields specially
    if (field.IsVector && field.VectorDimensions.HasValue) {
      return $"vector({field.VectorDimensions.Value})";
    }

    // Map .NET types to PostgreSQL types
    // The TypeName is fully qualified with global:: prefix
    var typeName = field.TypeName
        .Replace(PLACEHOLDER_GLOBAL, "")
        .TrimEnd('?'); // Remove nullable suffix

    return typeName switch {
      "System.Guid" => "UUID",
      "System.String" => "TEXT",
      "System.Int32" => "INTEGER",
      "System.Int64" => "BIGINT",
      "System.Int16" => "SMALLINT",
      "System.Boolean" => "BOOLEAN",
      "System.DateTime" => "TIMESTAMPTZ",
      "System.DateTimeOffset" => "TIMESTAMPTZ",
      "System.DateOnly" => "DATE",
      "System.TimeOnly" => "TIME",
      "System.Decimal" => "NUMERIC",
      "System.Double" => "DOUBLE PRECISION",
      "System.Single" => "REAL",
      "System.Byte[]" => "BYTEA",
      "float[]" => "REAL[]", // Array of floats
      "double[]" => "DOUBLE PRECISION[]",
      _ => "TEXT" // Default to TEXT for unknown types
    };
  }

  /// <summary>
  /// Determines if a perspective should be included in a DbContext based on key matching.
  /// </summary>
  /// <param name="perspective">The perspective to check</param>
  /// <param name="dbContext">The DbContext to check against</param>
  /// <returns>True if the perspective matches this DbContext's keys</returns>
  /// <remarks>
  /// Matching rules:
  /// - Perspective with no keys (empty array) matches default DbContext only (Key = "")
  /// - Otherwise, any perspective key must match any DbContext key
  /// </remarks>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_WithSingleKey_DiscoversDbContextWithKeyAsync</tests>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_WithMultipleKeys_DiscoversDbContextWithAllKeysAsync</tests>
  private static bool _matchesDbContext(PerspectiveModelInfo perspective, DbContextInfo dbContext) {
    // Perspective with no keys = matches default DbContext only
    if (perspective.Keys.Length == 0) {
      return dbContext.Keys.Contains("");
    }

    // Check if any perspective key matches any DbContext key
    return perspective.Keys.Any(pk => dbContext.Keys.Contains(pk));
  }

