    sb.AppendLine("      }");
    sb.AppendLine(CLOSE_BRACE_INDENT_4);
    sb.AppendLine();
  }

  /// <summary>
  /// Generates DbContext schema initialization extensions.
  /// Creates EnsureWhizbangDatabaseInitializedAsync() method for each discovered DbContext.
  /// Core infrastructure schema is generated at runtime by PostgresSchemaBuilder.
  /// Embeds PostgreSQL migration scripts for functions.
  /// Uses template system for code generation.
  /// IMPORTANT: Generates for ALL DbContexts, even those without perspectives.
  /// Inbox/Outbox/EventStore tables still need to be created.
  /// </summary>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_WithDiscoveredDbContext_GeneratesSchemaExtensionsAsync</tests>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_SchemaExtensions_CallsExecuteMigrationsAsync</tests>
  private static void _generateSchemaExtensions(
      SourceProductionContext context,
      ImmutableArray<PerspectiveModelInfo> perspectives,
      ImmutableArray<DbContextInfo> dbContexts,
      Compilation compilation) {

    if (dbContexts.IsEmpty) {
      return; // No DbContext found
    }

    // Load template once (same template for all DbContexts)
    var assembly = typeof(EFCoreServiceRegistrationGenerator).Assembly;
    var templateBase = TemplateUtilities.GetEmbeddedTemplate(
        assembly,
        "DbContextSchemaExtensionTemplate.cs",
        "Whizbang.Data.EFCore.Postgres.Generators.Templates"
    );

    // Load migration files (once for all DbContexts)
    // Note: Core infrastructure schema is now generated at runtime by PostgresSchemaBuilder
    string migrationsCode = _generateMigrationsCode(context);

    // Loop through each DbContext and generate extension method
    foreach (var dbContext in dbContexts) {
      // Filter perspectives that match this DbContext's keys (may be empty)
      var matchingPerspectives = perspectives.IsEmpty
          ? []
          : perspectives
              .Where(p => _matchesDbContext(p, dbContext))
              .ToList();

      // Generate perspective tables schema SQL (specific to this DbContext)
      string perspectiveTablesSchema = _generatePerspectiveTablesSchema(context, matchingPerspectives, dbContext.Schema);
      // Generate per-perspective entries for individual hash tracking
      string perspectiveEntriesCode = _generatePerspectiveEntriesCode(matchingPerspectives, dbContext.Schema);

      var template = templateBase;

      // Replace header with timestamp
      template = TemplateUtilities.ReplaceRegion(
          template,
          "HEADER",
          $"// <auto-generated/>\n// Generated by Whizbang.Data.EFCore.Postgres.Generators.EFCoreServiceRegistrationGenerator at {System.DateTime.UtcNow:yyyy-MM-dd HH:mm:ss} UTC\n// DO NOT EDIT - Changes will be overwritten\n#nullable enable"
      );

      // Note: CORE_INFRASTRUCTURE_SCHEMA is no longer replaced - template calls PostgresSchemaBuilder at runtime

      // Replace PERSPECTIVE_TABLES_SCHEMA placeholder with embedded perspective tables SQL
      // Note: perspectiveTablesSchema already includes @"..." wrapping from _generatePerspectiveTablesSchema
      template = template.Replace("__PERSPECTIVE_TABLES_SCHEMA__", perspectiveTablesSchema);
      // Replace PERSPECTIVE_ENTRIES region with per-perspective (name, sql) tuples for hash tracking
      template = TemplateUtilities.ReplaceRegion(template, "PERSPECTIVE_ENTRIES", perspectiveEntriesCode);

      // Replace MIGRATIONS region with embedded migration scripts
      template = TemplateUtilities.ReplaceRegion(
          template,
          "MIGRATIONS",
          migrationsCode
      );

      // Get assembly name for service identification
      var assemblyName = compilation.AssemblyName ?? "Unknown";

      // Replace REGISTER_ASSOCIATIONS region with call to RegisterPerspectiveAssociationsAsync
      // Only generate the call if this DbContext has matching perspectives (otherwise the extension method won't exist)
      string registerAssociationsCode;
      if (matchingPerspectives.Count > 0) {
        registerAssociationsCode = $"await dbContext.RegisterPerspectiveAssociationsAsync(\"{dbContext.Schema}\", \"{assemblyName}\", logger, cancellationToken);";
      } else {
        registerAssociationsCode = "// No perspectives found for this DbContext - skipping association registration";
      }
      template = TemplateUtilities.ReplaceRegion(
          template,
          "REGISTER_ASSOCIATIONS",
          registerAssociationsCode
      );

      // Generate perspective registry JSON for CLR type → table name tracking
      string perspectiveRegistryJson = _generatePerspectiveRegistryJson(matchingPerspectives, assemblyName);

      // Replace placeholders
      template = template.Replace("__DBCONTEXT_NAMESPACE__", dbContext.Namespace);
      template = template.Replace("__DBCONTEXT_CLASS__", dbContext.ClassName);
      template = template.Replace("__DBCONTEXT_FQN__", dbContext.FullyQualifiedName);
      // __QUOTED_SCHEMA__ is used in SQL contexts where reserved keywords like "user" need quoting
      // Double quotes are escaped ("") for use inside C# verbatim string literals (@"...")
      template = template.Replace("__QUOTED_SCHEMA__", $"\"\"{dbContext.Schema}\"\"");
      // __SCHEMA__ is used in C# contexts (like HasDefaultSchema) where EF Core handles quoting
      template = template.Replace("__SCHEMA__", dbContext.Schema);
      // __PERSPECTIVE_REGISTRY_JSON__ contains CLR type → table name mappings with schema hash
      template = template.Replace("__PERSPECTIVE_REGISTRY_JSON__", perspectiveRegistryJson);
      // __SERVICE_NAME__ is the assembly name for service identification
      template = template.Replace("__SERVICE_NAME__", assemblyName);
      // __LIBRARY_VERSION__ is the Whizbang library version from the generator assembly
      var libraryVersion = typeof(EFCoreServiceRegistrationGenerator).Assembly
          .GetCustomAttribute<System.Reflection.AssemblyInformationalVersionAttribute>()?.InformationalVersion
          ?? typeof(EFCoreServiceRegistrationGenerator).Assembly.GetName().Version?.ToString()
          ?? "unknown";
      // Strip build metadata suffix (e.g., "0.9.4-local.62+abc123" → "0.9.4-local.62")
      var plusIdx = libraryVersion.IndexOf('+');
      if (plusIdx > 0) {
        libraryVersion = libraryVersion[..plusIdx];
      }
      template = template.Replace("__LIBRARY_VERSION__", libraryVersion);
      // __APPLICATION_VERSION__ is the consuming assembly name + version for tracking which app applied migrations
      var appVersion = compilation.Assembly.Identity.Version.ToString();
      var applicationVersion = $"{assemblyName}/{appVersion}";
      template = template.Replace("__APPLICATION_VERSION__", applicationVersion);

      context.AddSource($"{dbContext.ClassName}_SchemaExtensions.g.cs", template);

      // Report diagnostic
      var descriptor = new DiagnosticDescriptor(
          id: "EFCORE102",
          title: "DbContext Schema Extension Generated",
          messageFormat: "Generated EnsureWhizbangDatabaseInitializedAsync() extension for '{0}' with {1} matching perspective(s)",
          category: DIAGNOSTIC_CATEGORY,
          defaultSeverity: DiagnosticSeverity.Info,
          isEnabledByDefault: true);

      context.ReportDiagnostic(Diagnostic.Create(descriptor, Location.None, dbContext.ClassName, matchingPerspectives.Count));
    }
  }

  /// <summary>
  /// Generates code for migration tuples to embed in generated file.
  /// Reads migration SQL files from generator's own Templates/Migrations (authoritative source).
  /// NOTE: Templates/Migrations must be kept in sync with Whizbang.Data.Postgres/Migrations manually.
  /// </summary>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_SchemaExtensions_CallsExecuteMigrationsAsync</tests>
  private static string _generateMigrationsCode(SourceProductionContext context) {
    var sb = new StringBuilder();

    // Load migrations from generator's own embedded resources (Templates/Migrations)
    var assembly = typeof(EFCoreServiceRegistrationGenerator).Assembly;
    var resourcePrefix = $"{assembly.GetName().Name}.Templates.Migrations.";

    // Get all embedded SQL migration files
    var migrationResources = assembly.GetManifestResourceNames()
        .Where(name => name.StartsWith(resourcePrefix, StringComparison.Ordinal) && name.EndsWith(".sql", StringComparison.Ordinal))
        .OrderBy(name => name)
        .ToArray();

    // DIAGNOSTIC: Report what we found (informational level)
    var diagnosticDescriptor = new DiagnosticDescriptor(
        id: "WHIZ702",
        title: "EF Core Migration Files Discovery",
        messageFormat: "Found {0} migration files in {1} (prefix: {2}): {3}",
        category: DIAGNOSTIC_CATEGORY,
        defaultSeverity: DiagnosticSeverity.Info,
        isEnabledByDefault: true);
    var resourceList = string.Join(", ", migrationResources.Take(5).Select(r => r[resourcePrefix.Length..]));
    if (migrationResources.Length > 5) {
      resourceList += $", ... and {migrationResources.Length - 5} more";
    }
    context.ReportDiagnostic(Diagnostic.Create(diagnosticDescriptor, Location.None,
        migrationResources.Length, assembly.GetName().Name, resourcePrefix, resourceList));

    if (migrationResources.Length == 0) {
      return "// No migration files found in embedded resources";
    }

    // Generate migration tuples
    for (int i = 0; i < migrationResources.Length; i++) {
      var resourceName = migrationResources[i];

      // Extract filename from resource name
      // From "Whizbang.Data.Postgres.Migrations.001_Name.sql" -> "001_Name.sql"
      // Or from "...Templates.Migrations.001_Name.sql" -> "001_Name.sql"
      var fileName = resourceName[resourcePrefix.Length..];

      // Read content from embedded resource
      using var stream = assembly.GetManifestResourceStream(resourceName);
      if (stream == null) {
        continue; // Skip if resource not found
      }

      using var reader = new System.IO.StreamReader(stream);
      var content = reader.ReadToEnd();

      // Escape the SQL content for C# verbatim string literal (@"...")
      // In verbatim strings, only quotes need escaping (by doubling them)
      // IMPORTANT: Also escape curly braces because ExecuteSqlRawAsync treats the string as a format string
      // IMPORTANT: Replace __SCHEMA__ with __MIGRATION_SCHEMA__ to prevent build-time replacement.
      //            The runtime _transformMigrationSql function uses the schema parameter, not __SCHEMA__.
      var escapedContent = content
          .Replace("__SCHEMA__", "__MIGRATION_SCHEMA__")  // Preserve for runtime transformation
          .Replace("\"", "\"\"")  // Escape quotes for verbatim string
          .Replace("{", "{{")     // Escape opening braces for ExecuteSqlRawAsync
          .Replace("}", "}}");    // Escape closing braces for ExecuteSqlRawAsync

      sb.Append($"      (\"{fileName}\", @\"{escapedContent}\")");

      if (i < migrationResources.Length - 1) {
        sb.AppendLine(",");
      }
    }

    return sb.ToString();
  }

  /// <summary>
  /// Generates CREATE TABLE statements for perspective tables.
  /// Inspects PerspectiveRow&lt;TModel&gt; to determine schema.
  /// PerspectiveRow has fixed schema: id (UUID PK), data (JSONB), metadata (JSONB), scope (JSONB), created_at, updated_at, version (INTEGER).
  /// Returns escaped SQL string ready to embed in C# verbatim string literal.
  /// </summary>
  /// <tests>tests/Whizbang.Generators.Tests/EFCoreServiceRegistrationGeneratorTests.cs:Generator_WithDiscoveredDbContext_GeneratesSchemaExtensionsAsync</tests>
  // S3776: SQL schema generation — complexity from per-field column types, index generation, and vector support
#pragma warning disable S3776
  private static string _generatePerspectiveTablesSchema(
    SourceProductionContext context,
    List<PerspectiveModelInfo> perspectives,
    string schema
  ) {
#pragma warning restore S3776
    if (perspectives.Count == 0) {
      return "\"\""; // Empty string - no perspective tables
    }

    // Quote schema name to handle PostgreSQL reserved keywords (e.g., "user", "table")
    var quotedSchema = _quotePostgresIdentifier(schema);

    var sb = new StringBuilder();
    sb.AppendLine("-- Perspective Tables (auto-generated from PerspectiveRow<TModel> types)");
    sb.AppendLine($"-- Generated: {DateTime.UtcNow:yyyy-MM-dd HH:mm:ss} UTC");
    sb.AppendLine($"-- Schema: {schema}");
    sb.AppendLine();

    // Create schema if it doesn't exist (quoted to handle reserved keywords like "user")
    sb.AppendLine($"CREATE SCHEMA IF NOT EXISTS {quotedSchema};");
    sb.AppendLine();

    // Check if any perspectives have vector fields - if so, ensure pgvector extension exists
    var hasVectorFields = perspectives.Any(p => p.PhysicalFields.Any(f => f.IsVector));
    if (hasVectorFields) {
      sb.AppendLine("-- Ensure pgvector extension exists for vector similarity search");
      sb.AppendLine("-- Using DO block to check first, avoiding permission errors when extension already exists");
      sb.AppendLine("-- (Required for Azure PostgreSQL which checks CREATE permissions before IF NOT EXISTS)");
      sb.AppendLine("DO $$");
      sb.AppendLine("BEGIN");
      sb.AppendLine("  IF NOT EXISTS (SELECT 1 FROM pg_extension WHERE extname = 'vector') THEN");
      sb.AppendLine("    CREATE EXTENSION vector;");
      sb.AppendLine("  END IF;");
      sb.AppendLine("END$$;");
      sb.AppendLine();
    }

    // Get unique tables (same table might be referenced by multiple perspectives)
    var uniqueTables = perspectives
      .GroupBy(p => p.TableName)
      .Select(g => g.First())
      .OrderBy(p => p.TableName)
      .ToList();

    foreach (var perspective in uniqueTables) {
      _appendCreateTableSql(sb, perspective, schema, quotedSchema);
      _appendStandardIndexes(sb, perspective, quotedSchema);
      _appendPhysicalFieldIndexes(sb, perspective, quotedSchema);
    }

    // Escape for C# verbatim string
    var sql = sb.ToString()
      .Replace("\"", "\"\"")  // Escape quotes
      .Replace("{", "{{")     // Escape braces for ExecuteSqlRawAsync
      .Replace("}", "}}");

    // Report success
    var descriptor = new DiagnosticDescriptor(
      id: "EFCORE206",
      title: "Perspective Tables Schema Generated",
      messageFormat: "Successfully generated schema for {0} perspective table(s) ({1} characters)",
      category: DIAGNOSTIC_CATEGORY,
      defaultSeverity: DiagnosticSeverity.Info,
      isEnabledByDefault: true
    );
    context.ReportDiagnostic(Diagnostic.Create(descriptor, Location.None, uniqueTables.Count, sql.Length));

    return $"@\"{sql}\"";
  }

  /// <summary>
  /// Appends CREATE TABLE SQL for a single perspective table.
  /// </summary>
  private static void _appendCreateTableSql(
      StringBuilder sb,
      PerspectiveModelInfo perspective,
      string schema,
      string quotedSchema) {
    // PerspectiveRow<TModel> has fixed schema defined in Whizbang.Core
    sb.AppendLine($"-- {schema}.{perspective.TableName} (model: {TypeNameUtilities.GetSimpleName(perspective.ModelTypeName)})");
    sb.AppendLine($"CREATE TABLE IF NOT EXISTS {quotedSchema}.{perspective.TableName} (");
    sb.AppendLine("  id UUID NOT NULL PRIMARY KEY,");
    sb.AppendLine("  data JSONB NOT NULL,");
    sb.AppendLine("  metadata JSONB NOT NULL,");
    sb.AppendLine("  scope JSONB NOT NULL,");
    sb.AppendLine("  created_at TIMESTAMPTZ NOT NULL,");
    sb.AppendLine("  updated_at TIMESTAMPTZ NOT NULL,");

    // Check if there are physical fields to add
    if (perspective.PhysicalFields.IsEmpty) {
      sb.AppendLine("  version INTEGER NOT NULL");
    } else {
      sb.AppendLine("  version INTEGER NOT NULL,");
      // Add physical fields
      for (int i = 0; i < perspective.PhysicalFields.Length; i++) {
        var field = perspective.PhysicalFields[i];
        var columnType = _getPostgresColumnType(field);
        var isLast = i == perspective.PhysicalFields.Length - 1;
        sb.AppendLine($"  {field.ColumnName} {columnType}{(isLast ? "" : ",")}");
      }
    }
    sb.AppendLine(");");
    sb.AppendLine();
  }

  /// <summary>
  /// Appends standard indexes (created_at B-tree, JSONB GIN) for a perspective table.
  /// </summary>
  private static void _appendStandardIndexes(
      StringBuilder sb,
      PerspectiveModelInfo perspective,
      string quotedSchema) {
    var shortName = perspective.TableName.Replace(PERSPECTIVE_TABLE_PREFIX, "");

    // Add B-tree index on created_at for time-based queries (matches EF Core configuration)
    sb.AppendLine($"CREATE INDEX IF NOT EXISTS idx_{shortName}_created_at");
    sb.AppendLine($"  ON {quotedSchema}.{perspective.TableName} (created_at);");
    sb.AppendLine();

    // Add GIN indexes on JSONB columns for full LINQ query support
    // GIN indexes enable efficient containment queries, key/value lookups, and path expressions
    sb.AppendLine($"CREATE INDEX IF NOT EXISTS idx_{shortName}_data_gin");
    sb.AppendLine($"  ON {quotedSchema}.{perspective.TableName} USING gin (data);");
    sb.AppendLine();
    sb.AppendLine($"CREATE INDEX IF NOT EXISTS idx_{shortName}_metadata_gin");
    sb.AppendLine($"  ON {quotedSchema}.{perspective.TableName} USING gin (metadata);");
    sb.AppendLine();
    sb.AppendLine($"CREATE INDEX IF NOT EXISTS idx_{shortName}_scope_gin");
    sb.AppendLine($"  ON {quotedSchema}.{perspective.TableName} USING gin (scope);");
    sb.AppendLine();
  }

  /// <summary>
  /// Appends indexes for physical fields marked with Indexed = true, including vector indexes.
  /// </summary>
  private static void _appendPhysicalFieldIndexes(
      StringBuilder sb,
      PerspectiveModelInfo perspective,
      string quotedSchema) {
    var shortName = perspective.TableName.Replace(PERSPECTIVE_TABLE_PREFIX, "");

    foreach (var field in perspective.PhysicalFields) {
      if (!field.IsIndexed) {
        continue;
      }

      if (field.IsVector && field.VectorDimensions.HasValue) {
        _appendVectorIndex(sb, field, shortName, perspective.TableName, quotedSchema);
      } else {
        // Regular B-tree index
        sb.AppendLine($"CREATE INDEX IF NOT EXISTS idx_{shortName}_{field.ColumnName}");
        sb.AppendLine($"  ON {quotedSchema}.{perspective.TableName} ({field.ColumnName});");
        sb.AppendLine();
      }
    }
  }

  /// <summary>
  /// Appends a vector (ivfflat) index for a physical field, or a comment if dimensions exceed the limit.
  /// </summary>
  private static void _appendVectorIndex(
      StringBuilder sb,
      PhysicalFieldInfo field,
      string shortName,
      string tableName,
      string quotedSchema) {
    // pgvector index dimension limits:
    // - ivfflat: max 2000 dimensions
    // - hnsw: max 2000 dimensions (pgvector < 0.7.0) or 16000 (pgvector >= 0.7.0)
    // To be safe with all pgvector versions, skip index for > 2000 dimensions
    // The column still works for queries, just without index acceleration
    var dimensions = field.VectorDimensions!.Value;
    if (dimensions <= 2000) {
      sb.AppendLine($"CREATE INDEX IF NOT EXISTS idx_{shortName}_{field.ColumnName}_vec");
      sb.AppendLine($"  ON {quotedSchema}.{tableName} USING ivfflat ({field.ColumnName} vector_cosine_ops);");
      sb.AppendLine();
    } else {
      sb.AppendLine($"-- NOTE: Skipping vector index for {field.ColumnName} ({dimensions} dimensions > 2000 limit)");
      sb.AppendLine("-- Vector queries will still work but without index acceleration");
      sb.AppendLine("-- To enable indexing, upgrade to pgvector >= 0.7.0 and manually create an hnsw index");
      sb.AppendLine();
    }
  }

  /// <summary>
  /// Generates per-perspective (Name, Sql) tuple entries for individual hash tracking.
  /// Each perspective gets its own CREATE TABLE + indexes SQL, enabling per-perspective
  /// change detection in the migration tracking system.
  /// </summary>
  private static string _generatePerspectiveEntriesCode(
      List<PerspectiveModelInfo> perspectives,
      string schema) {
    if (perspectives.Count == 0) {
      return "// No perspectives found for this DbContext";
    }

    var quotedSchema = _quotePostgresIdentifier(schema);
    var uniqueTables = perspectives
        .GroupBy(p => p.TableName)
        .Select(g => g.First())
        .OrderBy(p => p.TableName)
        .ToList();

    var sb = new StringBuilder();

    for (int i = 0; i < uniqueTables.Count; i++) {
      var perspective = uniqueTables[i];
      var perspSql = new StringBuilder();

      _generatePerspectiveTableSql(perspSql, perspective, quotedSchema);
      _generatePerspectiveIndexSql(perspSql, perspective, quotedSchema);

      var escapedSql = perspSql.ToString()
          .Replace("\"", "\"\"")
          .Replace("{", "{{")
          .Replace("}", "}}");

      var perspectiveName = TypeNameUtilities.GetSimpleName(perspective.ModelTypeName);

      sb.Append($"      (\"{perspectiveName}\", @\"{escapedSql}\")");
      if (i < uniqueTables.Count - 1) {
        sb.AppendLine(",");
      }
    }

    return sb.ToString();
  }

  private static void _generatePerspectiveTableSql(
      StringBuilder perspSql, PerspectiveModelInfo perspective, string quotedSchema) {
    perspSql.AppendLine($"CREATE TABLE IF NOT EXISTS {quotedSchema}.{perspective.TableName} (");
    perspSql.AppendLine("  id UUID NOT NULL PRIMARY KEY,");
    perspSql.AppendLine("  data JSONB NOT NULL,");
    perspSql.AppendLine("  metadata JSONB NOT NULL,");
    perspSql.AppendLine("  scope JSONB NOT NULL,");
    perspSql.AppendLine("  created_at TIMESTAMPTZ NOT NULL,");
    perspSql.AppendLine("  updated_at TIMESTAMPTZ NOT NULL,");

    if (perspective.PhysicalFields.IsEmpty) {
      perspSql.AppendLine("  version INTEGER NOT NULL");
    } else {
      perspSql.AppendLine("  version INTEGER NOT NULL,");
      for (int j = 0; j < perspective.PhysicalFields.Length; j++) {
        var field = perspective.PhysicalFields[j];
        var columnType = _getPostgresColumnType(field);
        var isLast = j == perspective.PhysicalFields.Length - 1;
        perspSql.AppendLine($"  {field.ColumnName} {columnType}{(isLast ? "" : ",")}");
      }
    }
    perspSql.AppendLine(");");
  }

  private static void _generatePerspectiveIndexSql(
      StringBuilder perspSql, PerspectiveModelInfo perspective, string quotedSchema) {
    var shortName = perspective.TableName.Replace(PERSPECTIVE_TABLE_PREFIX, "");
    perspSql.AppendLine($"CREATE INDEX IF NOT EXISTS idx_{shortName}_created_at ON {quotedSchema}.{perspective.TableName} (created_at);");
    perspSql.AppendLine($"CREATE INDEX IF NOT EXISTS idx_{shortName}_data_gin ON {quotedSchema}.{perspective.TableName} USING gin (data);");
    perspSql.AppendLine($"CREATE INDEX IF NOT EXISTS idx_{shortName}_metadata_gin ON {quotedSchema}.{perspective.TableName} USING gin (metadata);");
    perspSql.AppendLine($"CREATE INDEX IF NOT EXISTS idx_{shortName}_scope_gin ON {quotedSchema}.{perspective.TableName} USING gin (scope);");

    foreach (var field in perspective.PhysicalFields) {
      if (field.IsIndexed) {
        if (field.IsVector && field.VectorDimensions.HasValue && field.VectorDimensions.Value <= 2000) {
          perspSql.AppendLine($"CREATE INDEX IF NOT EXISTS idx_{shortName}_{field.ColumnName}_vec ON {quotedSchema}.{perspective.TableName} USING ivfflat ({field.ColumnName} vector_cosine_ops);");
        } else if (!field.IsVector) {
          perspSql.AppendLine($"CREATE INDEX IF NOT EXISTS idx_{shortName}_{field.ColumnName} ON {quotedSchema}.{perspective.TableName} ({field.ColumnName});");
        }
      }
    }
  }

  /// <summary>
  /// Generates perspective registry JSON for CLR type → table name tracking.
  /// Used by reconcile_perspective_registry() function for schema drift detection.
  /// </summary>
  /// <param name="perspectives">List of perspectives to register</param>
  /// <param name="serviceName">Assembly name for service identification</param>
  /// <returns>JSON string ready for C# embedding (escaped quotes)</returns>
  private static string _generatePerspectiveRegistryJson(
      List<PerspectiveModelInfo> perspectives,
      string serviceName) {

    if (perspectives.Count == 0) {
      return "\"[]\"";  // Empty JSON array as C# string literal
    }

    var sb = new StringBuilder();
    sb.Append('[');

    for (int i = 0; i < perspectives.Count; i++) {
      if (i > 0) {
        sb.Append(',');
      }
      _appendPerspectiveRegistryEntry(sb, perspectives[i], serviceName);
    }

    sb.Append(']');

    // Escape for C# string literal (double the quotes)
    var json = sb.ToString().Replace("\"", "\\\"");
    return $"\"{json}\"";
  }

  private static void _appendPerspectiveRegistryEntry(StringBuilder sb, PerspectiveModelInfo perspective, string serviceName) {
    var schemaColumns = _buildPerspectiveSchemaColumns(perspective);
    var schemaIndexes = _buildPerspectiveSchemaIndexes(perspective);

    var tableSchema = new PerspectiveTableSchema(schemaColumns, schemaIndexes);
    var schemaJson = SchemaHashUtilities.ToCanonicalJson(tableSchema);
    var schemaHash = SchemaHashUtilities.ComputeSchemaHash(tableSchema);

    sb.Append('{');
    var dbClrTypeName = perspective.ModelTypeName.Replace(PLACEHOLDER_GLOBAL, "");
    sb.Append($"\"ClrTypeName\":\"{_escapeJsonString(dbClrTypeName)}\",");
    sb.Append($"\"TableName\":\"{_escapeJsonString(perspective.TableName)}\",");
    sb.Append($"\"SchemaJson\":{schemaJson},");
    sb.Append($"\"SchemaHash\":\"{schemaHash}\",");
    sb.Append($"\"ServiceName\":\"{_escapeJsonString(serviceName)}\"");
    sb.Append('}');
  }

  private static List<ColumnSchema> _buildPerspectiveSchemaColumns(PerspectiveModelInfo perspective) {
    var schemaColumns = new List<ColumnSchema> {
      new("id", "uuid", false, true, false, null),
      new("data", "jsonb", false, false, false, null),
      new("metadata", "jsonb", false, false, false, null),
      new("scope", "jsonb", false, false, false, null),
      new("created_at", "timestamptz", false, false, false, null),
      new("updated_at", "timestamptz", false, false, false, null),
      new("version", "integer", false, false, false, null)
    };

    foreach (var field in perspective.PhysicalFields) {
      var postgresType = _getPostgresColumnType(field).ToLowerInvariant();
      schemaColumns.Add(new ColumnSchema(
          field.ColumnName,
          postgresType,
          true,
          false,
          field.IsVector,
          field.VectorDimensions
      ));
    }

    return schemaColumns;
  }

  private static List<IndexSchema> _buildPerspectiveSchemaIndexes(PerspectiveModelInfo perspective) {
    var shortName = perspective.TableName.Replace(PERSPECTIVE_TABLE_PREFIX, "");
    var schemaIndexes = new List<IndexSchema> {
      new($"idx_{shortName}_created_at", ["created_at"], "btree", false),
      new($"idx_{shortName}_data_gin", ["data"], "gin", false),
      new($"idx_{shortName}_metadata_gin", ["metadata"], "gin", false),
      new($"idx_{shortName}_scope_gin", ["scope"], "gin", false)
    };

    foreach (var field in perspective.PhysicalFields) {
      if (!field.IsIndexed) {
        continue;
      }

      if (field.IsVector && field.VectorDimensions.HasValue && field.VectorDimensions.Value > 2000) {
        continue;
      }

      var indexType = field.IsVector ? "ivfflat" : "btree";
      schemaIndexes.Add(new IndexSchema(
          $"idx_{shortName}_{field.ColumnName}" + (field.IsVector ? "_vec" : ""),
          [field.ColumnName],
          indexType,
          false
      ));
    }

    return schemaIndexes;
  }

  /// <summary>
  /// Escapes a string for use in JSON (handles special characters).
  /// </summary>
  private static string _escapeJsonString(string value) {
    return value
        .Replace("\\", "\\\\")
        .Replace("\"", "\\\"")
        .Replace("\n", "\\n")
        .Replace("\r", "\\r")
        .Replace("\t", "\\t");
  }
}

